    private void add4ActionPerformed(java.awt.event.ActionEvent evt) {                                      
        // TODO add your handling code here:
        Connection con = getConnection();
        try {
            
            st = (Statement) con.createStatement();
            String query = "INSERT INTO Invoice(InvoiceId,InvoiceDate,InvoicePrice, BookingId,StaffId) VALUES('" + jTextFieldInvoiceId.getText() + "',"
            + "'" + jTextFieldInvoiceDate.getText() + "','" + jTextFieldInvoicePrice.getText() + "', '" + jTextFieldBookingId.getText() + "', '" + jTextFieldStaffId.getText() + "')";

            st.execute(query);
            showInvoiceList();

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }                                     

    private void edit4ActionPerformed(java.awt.event.ActionEvent evt) {                                     
        // TODO add your handling code here:
        if (jTableInvoice.getSelectedRow()==-1) {
            if (jTableInvoice.getRowCount()==0) {
                // lblError.setText("Table is empty");
            }
            else{
                //  lblError.setText("You must select a Tennis Player");
            }
        }
        else{
            DefaultTableModel model = (DefaultTableModel) jTableInvoice.getModel();
            model.setValueAt(jTextFieldInvoiceId.getText(), jTableInvoice.getSelectedRow(), 0);
            model.setValueAt(jTextFieldInvoiceDate.getText().toString(), jTableInvoice.getSelectedRow(), 1);
            model.setValueAt(jTextFieldInvoicePrice.getText(), jTableInvoice.getSelectedRow(), 2);
            model.setValueAt(jTextFieldBookingId.getText(), jTableInvoice.getSelectedRow(), 3);
            model.setValueAt(jTextFieldStaffId.getText(), jTableInvoice.getSelectedRow(), 4);
        }

    }                                    

