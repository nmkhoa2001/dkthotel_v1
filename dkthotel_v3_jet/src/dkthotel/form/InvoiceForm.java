    public ArrayList<Invoice> getInvoiceList() {
        ArrayList<Invoice> ai = new ArrayList<Invoice>();
        Connection con = getConnection();
        try {
            st = (Statement) con.createStatement();
            String sql = "SELECT * FROM Invoice";
           
            ResultSet rs = st.executeQuery(sql);

            Invoice iv;
            while (rs.next()) {
                iv = new Invoice(rs.getString("InvoiceId"), rs.getDate("InvoiceDate"), rs.getFloat("InvoicePrice"), rs.getString("BookingId"), rs.getString("StaffId") );

               
                ai.add(iv);
            }
        } catch (Exception ex) {
            ex.printStackTrace();

        }
        return ai;
    }
     public void showInvoiceList() {
        String colTitle1[] = new String[]{"Invoice Id", "Invoice Date", "Invoice Price", "Booking Id", "Staff Id"};
        ArrayList<Invoice> ai = getInvoiceList();

        DefaultTableModel model = new DefaultTableModel(colTitle1, 0);

        Object[] row;

        for (int i = 0; i < ai.size(); i++) {

            row = new Object[5];

            // GÁN GIÁ TRỊ
            row[0] = ai.get(i).getInvoiceId();
            row[1] = ai.get(i).getInvoiceDate();
            row[2] = ai.get(i).getInvoicePrice();
            row[3] = ai.get(i).getBookingId();
            row[4] = ai.get(i).getStaffId();

            model.addRow(row);
        }
        
        //    }catch(ArrayIndexOutOfBoundsException ex){

        jTableInvoice.setModel(model);

    }

