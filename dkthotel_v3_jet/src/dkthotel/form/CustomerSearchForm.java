    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String TK[]={
                "Customer ID","Customer Name","IdentifyCard","Nationality","CustomerGender","CustomerDob","CustomerPhone", "BookingId"

        };
        DefaultTableModel modelTable=new DefaultTableModel(TK,0);
        try{
            st=con.createStatement();
            String sql="select * from Customer ";
            if(jTextFieldCustomerSearch.getText().length()>0){
                sql=sql + "where CustomerId like '%"+jTextFieldCustomerSearch.getText()+"%'";
            }
            rs=st.executeQuery(sql);
            modelTable.setRowCount(0);
            if(rs.isBeforeFirst()==false){
                JOptionPane.showMessageDialog(this, "No data!");
            }
            Vector data=null;
            while (rs.next()){
                data=new Vector();
                data.add(rs.getString("CustomerId"));
                data.add(rs.getString("CustomerName"));
                data.add(rs.getString("IdentifyCard"));
                data.add(rs.getString("Nationality"));
                data.add(rs.getString("CustomerGender"));
                data.add(rs.getDate("CustomerDob"));
                data.add(rs.getString("CustomerPhone"));
                data.add(rs.getString("BookingId"));
                modelTable.addRow(data);
            }
            jTableCustomerSearch.setModel(modelTable);


        }catch(Exception e){
            e.printStackTrace();

        }

    }

